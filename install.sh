# Logging functions
LOGI() {
    echo -e "${green}[INFO] $* ${plain}"
}

LOGD() {
    echo -e "${yellow}[DEBUG] $* ${plain}"
}

LOGE() {
    echo -e "${red}[ERROR] $* ${plain}"
}

